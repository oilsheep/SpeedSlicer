// Language display names
const languageNames = {
  'zh-TW': '繁體中文',
  'zh-CN': '简体中文',
  en: 'English',
  ja: '日本語',
  ko: '한국어',
};

let currentLang = 'en';

function detectLanguage() {
  const stored = localStorage.getItem('speedslicer-lang');
  if (stored && translations[stored]) return stored;

  const nav = navigator.language || navigator.userLanguage || 'en';
  // zh-TW, zh-Hant → zh-TW
  if (/^zh.*(TW|Hant)/i.test(nav)) return 'zh-TW';
  // zh, zh-CN, zh-Hans → zh-CN
  if (/^zh/i.test(nav)) return 'zh-CN';
  if (/^ja/i.test(nav)) return 'ja';
  if (/^ko/i.test(nav)) return 'ko';
  if (/^en/i.test(nav)) return 'en';
  return 'en';
}

function t(key, params = {}) {
  const str = translations[currentLang]?.[key] || translations.en[key] || key;
  return str.replace(/\{(\w+)\}/g, (_, k) => params[k] ?? '');
}

function setLanguage(lang) {
  if (!translations[lang]) lang = 'en';
  currentLang = lang;
  localStorage.setItem('speedslicer-lang', lang);
  applyTranslations();
}

function applyTranslations() {
  // data-i18n="key" → textContent
  document.querySelectorAll('[data-i18n]').forEach((el) => {
    el.textContent = t(el.dataset.i18n);
  });
  // data-i18n-title="key" → title attribute
  document.querySelectorAll('[data-i18n-title]').forEach((el) => {
    el.title = t(el.dataset.i18nTitle);
  });
  // data-i18n-placeholder="key" → placeholder attribute
  document.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  });

  // Update labels with dynamic values (sliders)
  document.querySelectorAll('[data-i18n-label]').forEach((el) => {
    const key = el.dataset.i18nLabel;
    const valSpan = el.querySelector('span');
    const suffix = el.dataset.i18nSuffix || '';
    if (valSpan) {
      el.childNodes[0].textContent = t(key) + ' ';
      // Keep the span and suffix intact
    } else {
      el.textContent = t(key);
    }
  });

  // Update language selector display
  const langSelect = document.getElementById('lang-select');
  if (langSelect) langSelect.value = currentLang;

  // CSS pseudo-element for loading text
  document.documentElement.style.setProperty('--loading-text', `"${t('processing')}"`);
}

export { translations, languageNames, currentLang, detectLanguage, t, setLanguage, applyTranslations };
